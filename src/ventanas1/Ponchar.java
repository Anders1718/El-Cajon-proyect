    void sumaHoras(){
        int suma=0;
        int horas=0;
        int minutos=0;
        System.out.println("Entré");
        for (int i = 0; i < modelo.getRowCount(); i++) {
            if(modelo.getValueAt(i, 3).toString().equals("ENTRADA")){
                System.out.println("Entré");
                String horario = modelo.getValueAt(i, 2).toString();
                String hora = horario.substring(11,12);
                String minuto = horario.substring(14,15);
                
                String horario2 = modelo.getValueAt(i+1, 2).toString();
                String hora2 = horario2.substring(11,12);
                String minuto2 = horario2.substring(14,15);
                
                horas = horas +(Integer.parseInt(hora2)-Integer.parseInt(hora));
                minutos = minutos + Math.abs(Integer.parseInt(minuto2) - Integer.parseInt(minuto));
                     
                i++;
            }
        }
        suma =(horas*60)+minutos;
        jLabelMinutos.setText(String.valueOf(suma));
        suma = horas + (minutos/60) ;
        jLabelHoras.setText(String.valueOf(suma));
        
    }
     
    
    
    
